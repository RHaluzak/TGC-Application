    private void solveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solveActionPerformed
        //Calculates the total cost of of travel upon click.
        
        double Miles;
        double PerGallon; //Cost of gas per gallon.
        double Cost;
        double Size;
        String costString;
        
        NumberFormat formatter = NumberFormat.getCurrencyInstance();
        
        //Gets input form textboxes. 
       Miles = Double.parseDouble(miles.getText());
       PerGallon = Double.parseDouble(perGallon.getText());
       Size = Double.parseDouble(size.getText());
       
       //Actual caculation.
       Cost = (Miles*PerGallon)/Size;
       costString = formatter.format(Cost);
       cost.setText(costString);
       
    }//GEN-LAST:event_solveActionPerformed

