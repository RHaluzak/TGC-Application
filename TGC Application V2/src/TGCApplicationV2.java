    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
           try{
               String Start, Destination, Miles, PerGallon, Cost, Size;
               
           //Gets input from textboxes.
           Start = start.getText();
           Destination = destination.getText();
           Miles = miles.getText();
           PerGallon = perGallon.getText();
           Size = size.getText();
           Cost = cost.getText();
           
           
           File trip = new File(Destination + ".txt"); //Creates text file using destination as name.
           FileWriter fw = new FileWriter(trip.getAbsoluteFile()); //Opens file for editing.
           BufferedWriter bw = new BufferedWriter(fw); //Writes to file.
           
           //Writes to text file. 
           bw.write("Starting Point:" + Start);
           bw.newLine(); //Creates new line.
           bw.write("Destination: " + Destination);
           bw.newLine();
           bw.write("Miles Traveled: " + Miles);
           bw.newLine();
           bw.write("Cost of gas per gallon: $" + PerGallon);
           bw.newLine();
           bw.write("Size of the gas tank in gallons: " + Size);
           bw.newLine();
           bw.newLine();
           bw.write("And the total cost was: " + Cost);
           bw.close();
           
           }catch (IOException e){
               e.printStackTrace();
           }
    }//GEN-LAST:event_saveActionPerformed

